// switch is used when we have to check for the multiple number of condition.

const month = 4;
switch(month) {
    case 1:
        console.log("january");
        break;
    case 2:
        console.log("feburary");
        break;
    case 3:
        console.log("march");
        break;    
    default:
        console.log("this is the defaule value");// this gives the default value if any of the case dosenot match.
        break;
}